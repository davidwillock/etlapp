package com.charlie1.etl.model;

public class RetailStoreDimData {
	
	
	String InstallDate;
	String TerminalID;
	String CustID;
	String RetailName;
	String RetailAddress;
	String County;
	String Region;
	String Country;
	
	
	public RetailStoreDimData() {}
	
	
	
	
	String StoreID;
	public String getStoreID() {
		return StoreID;
	}
	public void setStoreID(String storeID) {
		StoreID = storeID;
	}
	public String getInstallDate() {
		return InstallDate;
	}
	public void setInstallDate(String installDate) {
		InstallDate = installDate;
	}
	public String getTerminalID() {
		return TerminalID;
	}
	public void setTerminalID(String terminalID) {
		TerminalID = terminalID;
	}
	public String getCustID() {
		return CustID;
	}
	public void setCustID(String custID) {
		CustID = custID;
	}
	public String getRetailName() {
		return RetailName;
	}
	public void setRetailName(String retailName) {
		RetailName = retailName;
	}
	public String getRetailAddress() {
		return RetailAddress;
	}
	public void setRetailAddress(String retailAddress) {
		RetailAddress = retailAddress;
	}
	public String getCounty() {
		return County;
	}
	public void setCounty(String county) {
		County = county;
	}
	public String getRegion() {
		return Region;
	}
	public void setRegion(String region) {
		Region = region;
	}
	public String getCountry() {
		return Country;
	}
	public void setCountry(String country) {
		Country = country;
	}

	
	

	
	
	
	
}
